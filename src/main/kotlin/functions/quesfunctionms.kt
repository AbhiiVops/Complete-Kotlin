    // // Write different functions for calculating sum,
    //// difference, product, and division of two numbers and return each of them

    println(calsum(5,7))
    println(caldiff(8,7))
    println(caldiff(8,7))
    println(caldivision(4,2))




}

fun calsum(a:Int,b:Int) : Int {

    return a+b
}

fun caldiff(a:Int,b:Int) : Int {

    return a-b
}

fun calproduct(a:Int,b:Int) : Int {

    return a*b
}

fun caldivision(a:Int,b:Int) : Int {

    return a/b
}
